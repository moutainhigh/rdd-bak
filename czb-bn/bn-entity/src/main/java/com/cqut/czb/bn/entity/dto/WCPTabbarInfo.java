package com.cqut.czb.bn.entity.dto;

public class WCPTabbarInfo {
    private String url;
    private String text;
    private String imgNormal;
    private String imgClick;

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getImgNormal() {
        return imgNormal;
    }

    public void setImgNormal(String imgNormal) {
        this.imgNormal = imgNormal;
    }

    public String getImgClick() {
        return imgClick;
    }

    public void setImgClick(String imgClick) {
        this.imgClick = imgClick;
    }
}
