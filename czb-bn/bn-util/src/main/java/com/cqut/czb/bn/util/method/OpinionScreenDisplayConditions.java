package com.cqut.czb.bn.util.method;

public class OpinionScreenDisplayConditions {
    private Integer dateType;
    private String date;
    private String dateStart;
    private String dateMonth;
    private String dateQuarter;
    private String dateYear;
    private String someTime;

    public Integer getDateType() {
        return dateType;
    }

    public void setDateType(Integer dateType) {
        this.dateType = dateType;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getDateMonth() {
        return dateMonth;
    }

    public void setDateMonth(String dateMonth) {
        this.dateMonth = dateMonth;
    }

    public String getDateQuarter() {
        return dateQuarter;
    }

    public void setDateQuarter(String dateQuarter) {
        this.dateQuarter = dateQuarter;
    }

    public String getDateYear() {
        return dateYear;
    }

    public void setDateYear(String dateYear) {
        this.dateYear = dateYear;
    }

    public String getDateStart() {
        return dateStart;
    }

    public void setDateStart(String dateStart) {
        this.dateStart = dateStart;
    }

    public String getSomeTime() {
        return someTime;
    }

    public void setSomeTime(String someTime) {
        this.someTime = someTime;
    }
}
